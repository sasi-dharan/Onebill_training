package com.onebill.corejava;

public class TypeConversion {
	public static void main(String[] args) {
		int x=10;
		long y=x;
		System.out.println(y);
		
		long a= 100;
		int b=(int)a;
		System.out.println(b);
		
		char z=98;
		System.out.println(z);
		
		int r='y';
		System.out.println(r);
		
		
	}

}
